
package multiplicationtable;

public class Multiplicationtable {

    public static void main(String[] args) {
        for (int i =1;i<=10;i++ ){
        for (int j = 1;j<=10;j++){
            System.out.print(i+"x"+j+"="+(j*i));    
            System.out.print("\t\t");  
            
        }    
            System.out.println("");    
            
        }
       
    }
    
}
